// declare the setup function
function setup() {

    // create a canvas 500px by 500px
    createCanvas( 500, 500 );

    // color the background 'green'
    background( 'green' );
}

// declare the draw function
function draw() {

    // tell p5 to print the text string "Hello friends!"
    // this will be positioned at ( x: 50, y: 100 ).
    text( "Hello friends!", 50, 100 );
}
